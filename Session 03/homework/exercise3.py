# Để xóa 1 phần từ trong list ta sử dụng del statement
# Sau đây là 1 ví dụ
numbers = [1, 2, 3, 4, 5, 6, 7]
print('Numbers:', numbers)
print('\n del numbers [6]')
# Xóa phần từ tại index 6
del numbers[6]
print('Numbers:', numbers)

print('\n del numbers[1:4]')
del numbers[1:4]
print('Numbers:', numbers)



